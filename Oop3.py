class Math:
    
    @staticmethod
    def add(x,y):
        return x+y
    
    @staticmethod
    def sub(x,y):
        return x-y
    
    @staticmethod
    def mul(x,y):
        return x*y
    
    @staticmethod
    def div(x,y):
        return x/y

print(Math.add(2,3))
print(Math.sub(15,5))
print(Math.mul(12,3))
print(Math.div(9,3))